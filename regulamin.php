<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'>§ 10</b></p>

<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'>Prawo odstąpienia od umowy</b></p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>1.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Konsument może w terminie 14 dni odstąpić od Umowy
Sprzedaży bez podania jakiejkolwiek przyczyny.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>2.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Bieg terminu określonego w ust. 1 rozpoczyna się od
dostarczenia Produktu Konsumentowi lub wskazanej przez niego osobie innej niż
przewoźnik.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>3.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>W przypadku Umowy, która obejmuje wiele Produktów,
które są dostarczane osobno, partiami lub w częściach, termin wskazany w ust. 1
biegnie od dostawy ostatniej rzeczy, partii lub części.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>4.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>W przypadku Umowy, która polega na regularnym
dostarczaniu Produktów przez czas oznaczony (prenumerata), termin wskazany w
ust. 1 biegnie od objęcia w posiadanie pierwszej z rzeczy.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>5.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Konsument może odstąpić od Umowy, składając Sprzedawcy
oświadczenie o odstąpieniu od Umowy. Do zachowania terminu odstąpienia od Umowy
wystarczy wysłanie przez Konsumenta oświadczenia przed upływem tego terminu.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'>wersja dla sklepów, w których kupujący może przesłać </b></p>

<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'>oświadczenie o odstąpieniu od umowy w dowolny sposób</b></p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>6.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Oświadczenie może być wysłane za pomocą tradycyjnej
poczty, faxem bądź drogą elektroniczną poprzez przesłanie oświadczenia na adres
e-mail Sprzedawcy lub przez złożenie oświadczenia na stronie internetowej
Sprzedawcy - dane kontaktowe Sprzedawcy zostały określone w § 3. Oświadczenie
można złożyć również na formularzu, którego wzór stanowi załącznik nr 1 do
niniejszego Regulaminu oraz załącznik do ustawy z dnia 30 maja 2014 roku o
prawach konsumenta, jednak nie jest to obowiązkowe.</p>

<p class=MsoNormal align=center style='text-align:center'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'>wersja dla sklepów, w których kupujący może przesłać </b></p>

<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'>oświadczenie o odstąpieniu od umowy za pośrednictwem poczty </b></p>

<p class=MsoNormal style='margin-left:36.0pt;text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-left:36.0pt;text-align:justify'>6.
Oświadczenie może być wysłane za pomocą tradycyjnej poczty poprzez przesłanie
oświadczenia na adres Sprzedawcy - dane kontaktowe Sprzedawcy zostały określone
w § 3. Oświadczenie można złożyć również na formularzu, którego wzór stanowi
załącznik nr 1 do niniejszego Regulaminu oraz załącznik do ustawy z dnia 30
maja 2014 roku o prawach konsumenta, jednak nie jest to obowiązkowe.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>7.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>W przypadku przesłania oświadczenia przez Konsumenta
drogą elektroniczną, Sprzedawca niezwłocznie prześle Konsumentowi na podany przez
Konsumenta adres e-mail potwierdzenie otrzymania oświadczenia o odstąpieniu od
Umowy.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l20 level1 lfo4'><![if !supportLists]><span
style='mso-list:Ignore'>8.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Skutki odstąpienia od Umowy:</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l19 level1 lfo1'><![if !supportLists]><span
style='mso-list:Ignore'>a.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>W przypadku odstąpienia od Umowy zawartej na odległość
Umowę uważa się za niezawartą.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l19 level1 lfo1'><![if !supportLists]><span
style='mso-list:Ignore'>b.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>W przypadku odstąpienia od Umowy Sprzedawca zwraca
Konsumentowi niezwłocznie, nie później niż w terminie 14 dni od dnia otrzymania
oświadczenia Konsumenta o odstąpieniu od Umowy, wszystkie dokonane przez niego
płatności, w tym koszty dostarczenia rzeczy, z wyjątkiem dodatkowych kosztów
wynikających z wybranego przez Konsumenta sposobu dostarczenia innego niż
najtańszy zwykły sposób dostarczenia oferowany przez Sprzedawcę.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l19 level1 lfo1'><![if !supportLists]><span
style='mso-list:Ignore'>c.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Zwrotu płatności Sprzedawca dokona przy użyciu takich
samych metod płatności, jakie zostały przez Konsumenta użyte w pierwotnej
transakcji, chyba że Konsument wyraźnie zgodził się na inne rozwiązanie, które
nie będzie się wiązało dla niego z żadnymi kosztami.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l19 level1 lfo1'><![if !supportLists]><span
style='mso-list:Ignore'>d.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Sprzedawca może wstrzymać się ze zwrotem płatności do
czasu otrzymania Produktu z powrotem lub do czasu dostarczenia mu dowodu jego
odesłania, w zależności od tego, które zdarzenie nastąpi wcześniej.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l19 level1 lfo1'><![if !supportLists]><span
style='mso-list:Ignore'>e.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Konsument powinien odesłać Produkt na adres Sprzedawcy
podany w niniejszym Regulaminie niezwłocznie, nie później niż 14 dni od dnia, w
którym poinformował Sprzedawcę o odstąpieniu od Umowy. Termin zostanie
zachowany, jeśli Konsument odeśle Produkt przed upływem terminu 14 dni.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l19 level1 lfo1'><![if !supportLists]><span
style='mso-list:Ignore'>f.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Konsument ponosi bezpośrednie koszty zwrotu Produktu,
także koszty zwrotu Produktu, jeśli ze względu na swój charakter Produkt ten
nie mógł zostać w zwykłym trybie odesłany pocztą.</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l19 level1 lfo1'><![if !supportLists]><span
style='mso-list:Ignore'>g.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>Konsument odpowiada tylko za zmniejszenie wartości
Produktu wynikające z korzystania z niego w sposób inny niż było to konieczne
do stwierdzenia charakteru, cech i funkcjonowania Produktu.</p>

<p class=MsoNormal style='text-align:justify'>9. W przypadku gdy ze względu na
charakter Produktu nie może on zostać odesłany w zwykłym trybie pocztą,
informacja o tym, a także o kosztach zwrotu Produktu, będzie się znajdować w
opisie Produktu w Sklepie.</p>

<p class=MsoNormal style='text-align:justify'>10. Prawo do odstąpienia od umowy
zawartej na odległość nie przysługuje Konsumentowi w odniesieniu do Umowy:</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l5 level1 lfo6'><![if !supportLists]><span
style='mso-list:Ignore'>a.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>w której przedmiotem świadczenia jest rzecz
nieprefabrykowana, wyprodukowana według specyfikacji Konsumenta lub służąca
zaspokojeniu jego zindywidualizowanych potrzeb,</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l5 level1 lfo6'><![if !supportLists]><span
style='mso-list:Ignore'>b.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>w której przedmiotem świadczenia jest rzecz dostarczana
w zapieczętowanym opakowaniu, której po otwarciu opakowania nie można zwrócić
ze względu na ochronę zdrowia lub ze względów higienicznych, jeżeli opakowanie
zostało otwarte po dostarczeniu,</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l5 level1 lfo6'><![if !supportLists]><span
style='mso-list:Ignore'>c.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>w które przedmiotem świadczenia jest rzecz ulegająca
szybkiemu zepsuciu lub mająca krótki termin przydatności do użycia,</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l5 level1 lfo6'><![if !supportLists]><span
style='mso-list:Ignore'>d.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>o świadczenie usług, jeżeli Sprzedawca wykonał w pełni
usługę za wyraźną zgodą Konsumenta, który został poinformowany przez
rozpoczęciem świadczenia, że po spełnieniu świadczenia przez Sprzedawcę utraci
prawo odstąpienia od Umowy,</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l5 level1 lfo6'><![if !supportLists]><span
style='mso-list:Ignore'>e.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>w którym cena lub wynagrodzenie zależy od wahań na
rynku finansowym, nad którym Sprzedawca nie sprawuje kontroli, i które mogą
wystąpić przed upływem terminu do odstąpienia od Umowy,</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l5 level1 lfo6'><![if !supportLists]><span
style='mso-list:Ignore'>f.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>w której przedmiotem świadczenia są rzeczy, które po
dostarczeniu, ze względu na swój charakter, zostają nierozłącznie połączone z
innymi rzeczami,</p>

<p class=MsoNormal style='margin-left:36.0pt;mso-add-space:auto;text-align:
justify;text-indent:-18.0pt;mso-list:l5 level1 lfo6'><![if !supportLists]><span
style='mso-list:Ignore'>g.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;
</span></span><![endif]>w której przedmiotem świadczenia są napoje alkoholowe,
których cena została uzgodniona przy zawarciu umowy sprzedaży, a których
dostarczenie może nastąpić dopiero po upływie 30 dni i których wartość zależy
