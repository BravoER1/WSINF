<p class=MsoNormal align=center style='text-align:center'><b style='mso-bidi-font-weight:
normal'>Postanowienia końcowe</b></p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>1. Umowy zawierane poprzez Sklep
internetowy zawierane są w języku polskim.</p>

<p class=MsoNormal style='text-align:justify'>2. Sprzedawca zastrzega sobie
prawo do dokonywania zmian Regulaminu z ważnych przyczyn to jest: zmiany
przepisów prawa, zmiany sposobów płatności i dostaw - w zakresie, w jakim te
zmiany wpływają na realizację postanowień niniejszego Regulaminu. O każdej zmianie
Sprzedawca poinformuje Klienta z co najmniej 7 dniowym wyprzedzeniem.</p>

<p class=MsoNormal style='text-align:justify'>3. W sprawach nieuregulowanych w
niniejszym Regulaminie mają zastosowanie powszechnie obowiązujące przepisy
prawa polskiego, w szczególności: Kodeksu cywilnego; ustawy o świadczeniu usług
drogą elektroniczną; ustawy o prawach konsumenta, ustawy o ochronie danych
osobowych. </p>

<p class=MsoNormal style='text-align:justify'>4. Klient ma prawo skorzystać z
